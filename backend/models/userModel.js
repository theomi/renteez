const mongoose = require('mongoose')

const Schema = mongoose.Schema

const userSchema = new Schema({
    title: {
    type: String,
    required: true,
  },
  first_name: {
    type: String,
    required: true,
  },
  last_name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: false,
  },
}, { timestamps: true })

module.exports = mongoose.model('User', userSchema)